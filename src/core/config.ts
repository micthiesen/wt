/**
 * Config loader.
 *
 * Reads `config.toml` from (in order):
 *   1. `$WT_CONFIG` (explicit override)
 *   2. `$XDG_CONFIG_HOME/wt/config.toml`
 *   3. `~/.config/wt/config.toml`
 *
 * Required fields have no default — the loader collects every missing
 * or malformed field and exits with one human-readable error before
 * the TUI ever starts. Generic defaults (branch.base, slug_max_len,
 * etc.) are filled in transparently; values derived from the user's
 * branch prefix (stage.prefix, stage.default_personal) are computed
 * when omitted.
 *
 * The whole config resolves once at import time and is exposed as a
 * frozen `config` object plus named constants in `paths.ts`. Hot
 * reload is intentionally not supported — the TUI reads a stable
 * snapshot for its lifetime.
 */
import { existsSync, readFileSync } from "node:fs";
import { homedir } from "node:os";
import { join } from "node:path";

import type { BackendKind } from "./backend/types.ts";

const HOME = homedir();

function expandHome(p: string): string {
  if (p === "~") return HOME;
  if (p.startsWith("~/")) return join(HOME, p.slice(2));
  return p;
}


/**
 * Pre-built action surfaced by the `!` modal. Two flavors:
 *
 *   - `claude`: prompt action; headless launches the selected primary
 *     harness's non-interactive CLI (`claude -p`, `codex exec`, or
 *     `opencode run`) with the configured prompt; the edit modal
 *     exposes an extras textarea that gets appended.
 *   - `shell`: launches `$SHELL -lc <shell>` in the worktree path; the
 *     edit modal is skipped and Enter launches directly.
 *
 * Both `prompt` and `shell` (and the user-typed extras) support
 * `{{var}}` substitution. Vars are populated per-launch from the row
 * — see `buildActionVars` in `tui/app-helpers.ts` for the full set. Unknown
 * vars pass through unchanged so typos surface in the rendered prompt.
 *
 * `{{skill_prefix}}` is the harness skill-invocation prefix (`/` for
 * Claude Code, `$` for OpenCode / Codex), chosen per launch from the
 * row's primary harness.
 * Use it for any prompt that invokes a named skill: `{{skill_prefix}}restack`.
 *
 * When `[[actions]]` is absent the built-in defaults take effect; when
 * it's present (even with one entry), the user's list is authoritative
 * — the way to drop a default is to list everything except it.
 *
 * `affects` declares which state domains the action mutates so the
 * runner can invalidate the matching cache prefixes after the action
 * exits. Defaults: claude actions push commits → `["git", "github"]`;
 * shell actions are opaque → `[]` (opt in explicitly when needed). An
 * explicit `affects = []` opts out entirely — useful for a claude
 * action that just analyzes without writing.
 *
 * `requires` declares preconditions evaluated synchronously against
 * the current row state. The picker grays out entries whose
 * requirements aren't met (with the reason as the dim subtitle); the
 * keybinding launcher toasts the reason and skips the run. Tag
 * vocabulary:
 *   "pr"        — row has a PR (open, draft, terminal, any).
 *   "pr.ready"  — row has a non-draft, OPEN PR.
 *   "deployed"  — `isOurStageDeployed` (.sst/stage is pinned and prefix-
 *                 valid AND outputs.json references it). Matches what
 *                 `wt rm --destroy-stage` uses.
 * Default is `[]` (no preconditions).
 *
 * Both arrays are deduped at parse time. See the architecture block
 * in `state/hooks.ts` for the rules these tags participate in.
 */
export type EffectTag = "git" | "github";
export type RequireTag = "pr" | "pr.ready" | "deployed";

/**
 * Optional picker affordances shared by both action kinds. `key` is the
 * single-char quick-pick letter in the `!` menu (auto-derived from the
 * name when omitted; see `assignActionKeys`). `group` is a section
 * label; actions sharing a group cluster under one header in the picker.
 */
type ActionUi = {
  /** Quick-pick letter in the `!` picker. Omit to auto-derive. */
  key?: string;
  /** Section label; actions with the same group cluster together. */
  group?: string;
};

/**
 * Where a prompt action is delivered. `headless` (default) spawns a tracked
 * non-interactive run for the selected primary harness. `session` injects
 * the prompt into the worktree's live primary (F12) harness session, starting
 * it first if it isn't running, so the prompt lands in a conversation with
 * existing context and history. Session delivery is fire-and-forget: there's
 * no completion sentinel, so `affects` does not auto-refresh on finish.
 */
export type ClaudeActionTarget = "headless" | "session";

/**
 * Per-launch user-supplied value collected by the picker before the
 * action runs. When set, picking the action opens a second picker
 * showing recent values for this action (drawn from
 * `~/.cache/wt/action-history.json`); a trailing "new…" row opens a
 * single-line input for a fresh value. The collected value substitutes
 * `{{arg}}` in `prompt` / `shell`. `null` skips the second-step prompt
 * entirely (the existing direct-launch / claude-extras flow applies).
 *
 * The label rendered in history rows comes from a per-line regex match
 * against the run's captured output — see `labelExtract` below.
 * Actions without a regex (or whose run doesn't match) keep showing
 * the raw value.
 */
type ActionArgPrompt = {
  /** Picker / input modal header. Plain text. */
  label: string;
};

export type ActionDef =
  | ({
      kind: "claude";
      id: string;
      name: string;
      prompt: string;
      target: ClaudeActionTarget;
      affects: readonly EffectTag[];
      requires: readonly RequireTag[];
      argPrompt: ActionArgPrompt | null;
      /**
       * Optional regex (source pattern, no flags) wt scans the run's
       * captured output with — last per-line match wins, capture group
       * 1 becomes the history label (falls back to the full match when
       * the pattern has no capture group). Lets the script stay vendor-
       * neutral: it just emits whatever line is naturally useful and
       * the action def declares how to pull a label out of it. Null
       * skips label extraction; the picker shows the raw value.
       */
      labelExtract: string | null;
    } & ActionUi)
  | ({
      kind: "shell";
      id: string;
      name: string;
      shell: string;
      affects: readonly EffectTag[];
      requires: readonly RequireTag[];
      argPrompt: ActionArgPrompt | null;
      /** See the claude-variant docstring. */
      labelExtract: string | null;
    } & ActionUi);

/**
 * Trigger vocabulary for `[[automations]]`. Each is a LEVEL condition
 * evaluated against the aggregated row state (see
 * `tui/automation-rules.ts` for the exact predicate + fire-key shape):
 *
 *   "pr.checks.failed"        — open PR's checks rollup is failing.
 *   "rabbit.unresolved"       — CodeRabbit has unresolved threads.
 *   "review.changes_requested"— a human review requested changes.
 *   "pr.conflict"             — merge-tree probe says the branch
 *                               conflicts with its effective base.
 *   "wt.merged"               — a NON-stacked worktree's branch landed
 *                               (merged / gone / PR merged — same set
 *                               the `c` clean sweep uses).
 *   "stack.parent_merged"     — a stack (worktrees chained by their
 *                               recorded fork bases) has a merged
 *                               member with open members stacked on it.
 */
export type AutomationTrigger =
  | "pr.checks.failed"
  | "rabbit.unresolved"
  | "review.changes_requested"
  | "pr.conflict"
  | "wt.merged"
  | "stack.parent_merged";

/**
 * What to do when the target worktree isn't quiescent at delivery time
 * (live session working/asking, action running, recent edits):
 * `queue` (default) holds the intent and delivers once things settle;
 * `skip` marks the fire handled and does nothing. There is deliberately
 * no "force" — injecting into an `asking` session would answer its
 * permission dialog with the paste's trailing Enter.
 */
export type AutomationBusyPolicy = "queue" | "skip";

/** `run` targets that dispatch built-in flows instead of an [[actions]] def. */
export const AUTOMATION_BUILTINS = ["builtin:restack", "builtin:clean"] as const;
export type AutomationBuiltin = (typeof AUTOMATION_BUILTINS)[number];

/**
 * One `[[automations]]` binding: when `on` holds for a worktree (and
 * the fire key is unseen), run `run` — an `[[actions]]` id (inheriting
 * its target/affects/requires) or a `builtin:` flow. No defaults ship;
 * an absent section means nothing is automated.
 */
export type AutomationDef = {
  id: string;
  on: AutomationTrigger;
  /** An [[actions]] id, or one of AUTOMATION_BUILTINS. */
  run: string;
  busy: AutomationBusyPolicy;
  /** Minimum minutes between dispatches per (rule, worktree). Null = none. */
  cooldownMinutes: number | null;
  /**
   * Quiescence window: the intent must be at least this old AND the
   * worktree free of edits for this long before delivery. Doubles as
   * the human-cancellation grace period. Defaults per trigger: merge
   * triggers (`wt.merged`, `stack.parent_merged`) can't flap — a merge
   * doesn't un-happen — so they default short; the rest default long
   * enough to ride out CI/review churn.
   */
  settleSeconds: number;
};


function configPath(): { path: string; present: boolean } {
  const explicit = process.env.WT_CONFIG;
  if (explicit) {
    const p = expandHome(explicit);
    return { path: p, present: existsSync(p) };
  }
  const xdg = process.env.XDG_CONFIG_HOME;
  const dir = xdg ? expandHome(xdg) : join(HOME, ".config");
  const p = join(dir, "wt", "config.toml");
  return { path: p, present: existsSync(p) };
}

type Raw = Record<string, unknown>;

function obj(v: unknown): Raw | null {
  return v && typeof v === "object" && !Array.isArray(v) ? (v as Raw) : null;
}

function strArr(v: unknown, fallback: readonly string[]): readonly string[] {
  if (!Array.isArray(v)) return fallback;
  const out: string[] = [];
  for (const x of v) if (typeof x === "string") out.push(x);
  return out;
}

class Errors {
  private list: string[] = [];
  add(msg: string) {
    this.list.push(msg);
  }
  /** Read a required string field; record an error and return "" on miss. */
  reqStr(parent: Raw | null, section: string, key: string): string {
    const v = parent?.[key];
    if (typeof v === "string" && v.length > 0) return v;
    this.add(`${section}.${key} is required`);
    return "";
  }
  optStr(parent: Raw | null, key: string, fallback: string): string {
    const v = parent?.[key];
    return typeof v === "string" && v.length > 0 ? v : fallback;
  }
  optStrOrNull(parent: Raw | null, key: string): string | null {
    const v = parent?.[key];
    return typeof v === "string" && v.length > 0 ? v : null;
  }
  optNum(parent: Raw | null, key: string, fallback: number): number {
    const v = parent?.[key];
    return typeof v === "number" && Number.isFinite(v) ? v : fallback;
  }
  /** Read an optional string-enum field; record an error on a value outside `allowed`. */
  optEnum<T extends string>(
    parent: Raw | null,
    section: string,
    key: string,
    allowed: readonly T[],
    fallback: T,
  ): T {
    const v = parent?.[key];
    if (v === undefined) return fallback;
    if (typeof v === "string" && (allowed as readonly string[]).includes(v)) {
      return v as T;
    }
    this.add(`${section}.${key} must be one of: ${allowed.join(", ")}`);
    return fallback;
  }
  flush(configFile: string): void {
    if (this.list.length === 0) return;
    const lines = this.list.map((l) => `  - ${l}`).join("\n");
    process.stderr.write(
      `wt: invalid config at ${configFile}\n${lines}\n` +
        `\nSee src/core/config.ts for the full schema.\n`,
    );
    process.exit(1);
  }
}

function loadRaw(path: string, present: boolean, errs: Errors): Raw {
  if (!present) {
    errs.add(`config file not found at ${path}`);
    return {};
  }
  try {
    const text = readFileSync(path, "utf8");
    const parsed = Bun.TOML.parse(text);
    return obj(parsed) ?? {};
  } catch (err) {
    errs.add(`failed to parse: ${(err as Error).message}`);
    return {};
  }
}

function build(raw: Raw, errs: Errors): Config {
  const paths = obj(raw.paths);
  const branch = obj(raw.branch);
  const stage = obj(raw.stage);
  const lifecycle = obj(raw.lifecycle);
  const deploy = obj(raw.deploy);
  const sstRaw = deploy ? obj(deploy.sst) : null;
  const tracker = obj(raw.issue_tracker);
  const linearRaw = tracker ? obj(tracker.linear) : null;
  const remoteRaw = obj(raw.remote);
  const ui = obj(raw.ui);

  const branchPrefix = errs.reqStr(branch, "branch", "prefix");
  const branchBase = errs.optStr(branch, "base", GENERIC_DEFAULTS.branch.base);
  const idPattern = errs.optStr(branch, "id_pattern", GENERIC_DEFAULTS.branch.idPattern);
  const slugMaxLen = errs.optNum(branch, "slug_max_len", GENERIC_DEFAULTS.branch.slugMaxLen);

  const mainClone = expandHome(errs.reqStr(paths, "paths", "main_clone"));
  const worktreeRoot = expandHome(errs.reqStr(paths, "paths", "worktree_root"));
  const logDir = expandHome(errs.optStr(paths, "log_dir", GENERIC_DEFAULTS.paths.logDir));
  // Sibling subdir for structured app logs — keeps daily `wt-*.log`
  // files separate from the per-worktree `<slug>-*.log` destroy logs
  // that `wt logs <slug>` reads. Not user-configurable; fold into
  // `log_dir` if the user moved that.
  const appLogDir = join(logDir, "app");
  const lockDir = expandHome(errs.optStr(paths, "lock_dir", GENERIC_DEFAULTS.paths.lockDir));
  const cacheDb = expandHome(errs.optStr(paths, "cache_db", GENERIC_DEFAULTS.paths.cacheDb));
  const weztermCliRaw =
    errs.optStrOrNull(paths, "wezterm_cli") ?? GENERIC_DEFAULTS.paths.weztermCli;
  const weztermCli = weztermCliRaw ? expandHome(weztermCliRaw) : null;

  // Stage prefix derives from branch.prefix when omitted — the
  // overwhelmingly common shape is `<branch.prefix>-<stage>`. Same
  // for the personal stage name, which is just the bare prefix.
  const stagePrefix = errs.optStr(stage, "prefix", `${branchPrefix}-`);
  const stageDefault = errs.optStr(stage, "default_personal", branchPrefix);
  const stageDomain = errs.optStrOrNull(stage, "domain");

  const envFiles = strArr(lifecycle?.env_files_to_copy, GENERIC_DEFAULTS.lifecycle.envFilesToCopy);

  // Worktree backend — opt-in. Absent section (or absent key) means the
  // default `git-worktree`; `rift` switches new creates to CoW clones.
  const backendRaw = obj(raw.backend);
  const backendKind = errs.optEnum(
    backendRaw,
    "backend",
    "kind",
    ["git-worktree", "rift"] as const satisfies readonly BackendKind[],
    "git-worktree",
  );

  // SST and Linear are entirely optional — absence of the section
  // disables the integration. When the section IS present, every
  // required field within it must be set.
  const sst: SstConfig | null = sstRaw === null
    ? null
    : {
      stateBucket: errs.reqStr(sstRaw, "deploy.sst", "state_bucket"),
      statePrefix: errs.reqStr(sstRaw, "deploy.sst", "state_prefix"),
      awsProfile: errs.reqStr(sstRaw, "deploy.sst", "aws_profile"),
      autoRegenPaths: strArr(sstRaw.auto_regen_paths, GENERIC_DEFAULTS.sst.autoRegenPaths),
    };

  const linear: LinearConfig | null = linearRaw === null
    ? null
    : { workspace: errs.reqStr(linearRaw, "issue_tracker.linear", "workspace") };

  const remoteHost = remoteRaw === null
    ? ""
    : errs.reqStr(remoteRaw, "remote", "host");
  const remote: RemoteConfig | null = remoteRaw === null
    ? null
    : {
      host: remoteHost,
      label: errs.optStr(remoteRaw, "label", remoteHost),
      wtPath: errs.optStr(remoteRaw, "wt_path", "~/.wt/bin/wt"),
    };

  const aiRaw = obj(raw.ai);
  const aiProvider = errs.optEnum(
    aiRaw,
    "ai",
    "provider",
    ["openai", "gemini"] as const satisfies readonly AiProvider[],
    GENERIC_DEFAULTS.ai.provider,
  );
  const aiBase = aiRaw === null
    ? null
    : {
      model: errs.reqStr(aiRaw, "ai", "model"),
      maxInputTokens: errs.optNum(
        aiRaw,
        "max_input_tokens",
        GENERIC_DEFAULTS.ai.maxInputTokens,
      ),
      timeoutMs: errs.optNum(
        aiRaw,
        "timeout_ms",
        GENERIC_DEFAULTS.ai.timeoutMs,
      ),
    };
  const ai: AiConfig | null = aiBase === null
    ? null
    : aiProvider === "gemini"
      ? {
        provider: "gemini",
        endpoint: errs.optStr(
          aiRaw,
          "endpoint",
          GENERIC_DEFAULTS.ai.geminiEndpoint,
        ).replace(/\/+$/, ""),
        apiKeyEnv: errs.reqStr(aiRaw, "ai", "api_key_env"),
        ...aiBase,
      }
      : {
        provider: "openai",
        endpoint: errs.reqStr(aiRaw, "ai", "endpoint").replace(/\/+$/, ""),
        ...aiBase,
      };

  const diffRaw = obj(raw.diff);
  const diff: DiffConfig = {
    command: errs.optStr(diffRaw, "command", GENERIC_DEFAULTS.diff.command),
  };

  const rows = strArr(ui?.rows, GENERIC_DEFAULTS.ui.rows);
  const uiMode = errs.optEnum(
    ui,
    "ui",
    "mode",
    ["classic", "hub"] as const satisfies readonly TuiMode[],
    "classic",
  );
  const hubBackgroundRaw = errs.optStr(ui, "hub_background", GENERIC_DEFAULTS.ui.hubBackground);
  let hubBackground = GENERIC_DEFAULTS.ui.hubBackground;
  if (/^#[0-9a-fA-F]{6}$/.test(hubBackgroundRaw)) {
    hubBackground = hubBackgroundRaw;
  } else {
    errs.add("ui.hub_background must be a #RRGGBB hex color");
  }

  const githubRaw = obj(raw.github);
  const githubEventsRaw = githubRaw ? obj(githubRaw.events) : null;
  const githubEventsSecretFile = githubEventsRaw
    ? errs.optStrOrNull(githubEventsRaw, "secret_file")
    : null;
  const githubEvents: GithubEventsConfig | null = githubEventsRaw === null
    ? null
    : {
      port: errs.optNum(githubEventsRaw, "port", 8765),
      host: errs.optStr(githubEventsRaw, "host", "127.0.0.1"),
      secret: errs.optStrOrNull(githubEventsRaw, "secret"),
      secretFile: githubEventsSecretFile ? expandHome(githubEventsSecretFile) : null,
      backstopPollMs: errs.optNum(githubEventsRaw, "backstop_poll_ms", 600_000),
    };
  const github: GithubConfig = {
    ignoredChecks: strArr(githubRaw?.ignored_checks, []),
    defaultReviewer: errs.optStrOrNull(githubRaw, "default_reviewer"),
    prTarget: errs.optEnum(
      githubRaw,
      "github",
      "pr_target",
      ["github", "linear"] as const satisfies readonly PullRequestTarget[],
      GENERIC_DEFAULTS.github.prTarget,
    ),
    events: githubEvents,
  };

  // [[actions]] is fully replaced when the user provides any entries
  // (even just one) — otherwise the built-in defaults apply. That way
  // a user can drop a default they don't want simply by listing the
  // ones they keep.
  const actions = parseActions(raw.actions, errs);
  // [[automations]] has NO defaults — automation is strictly opt-in.
  // Parsed after actions so `run` references validate against the
  // final action list.
  const automations = parseAutomations(raw.automations, actions, errs);

  return {
    paths: {
      mainClone,
      worktreeRoot,
      logDir,
      appLogDir,
      lockDir,
      cacheDb,
      weztermCli,
    },
    branch: { prefix: branchPrefix, base: branchBase, idPattern, slugMaxLen },
    stage: { prefix: stagePrefix, defaultPersonal: stageDefault, domain: stageDomain },
    lifecycle: { envFilesToCopy: envFiles },
    backend: { kind: backendKind },
    sst,
    linear,
    remote,
    ai,
    diff,
    github,
    actions,
    automations,
    ui: { rows, mode: uiMode, hubBackground },
  };
}

const VALID_TRIGGERS = new Set<AutomationTrigger>([
  "pr.checks.failed",
  "rabbit.unresolved",
  "review.changes_requested",
  "pr.conflict",
  "wt.merged",
  "stack.parent_merged",
]);
const DEFAULT_SETTLE_SECONDS = 120;
// Merge-driven triggers are un-flappy, and the typical next step (review
// the next member, mark it ready) wants the restack done promptly — keep
// only a token cancellation window.
const MERGED_SETTLE_SECONDS = 10;
const MERGE_TRIGGERS: ReadonlySet<AutomationTrigger> = new Set([
  "wt.merged",
  "stack.parent_merged",
]);
function defaultSettleSeconds(on: AutomationTrigger): number {
  return MERGE_TRIGGERS.has(on) ? MERGED_SETTLE_SECONDS : DEFAULT_SETTLE_SECONDS;
}

function parseAutomations(
  raw: unknown,
  actions: readonly ActionDef[],
  errs: Errors,
): readonly AutomationDef[] {
  if (raw === undefined) return [];
  if (!Array.isArray(raw)) {
    errs.add("automations must be a TOML array of [[automations]] tables");
    return [];
  }
  const actionIds = new Set(actions.map((a) => a.id));
  const builtins = new Set<string>(AUTOMATION_BUILTINS);
  const out: AutomationDef[] = [];
  const seenIds = new Set<string>();
  for (let i = 0; i < raw.length; i++) {
    const entry = obj(raw[i]);
    const tag = `automations[${i}]`;
    if (!entry) {
      errs.add(`${tag} must be a table`);
      continue;
    }
    const id = errs.reqStr(entry, tag, "id");
    if (!id) continue;
    if (seenIds.has(id)) {
      errs.add(`${tag}.id "${id}" is duplicated`);
      continue;
    }
    const on = entry.on;
    if (typeof on !== "string" || !VALID_TRIGGERS.has(on as AutomationTrigger)) {
      errs.add(`${tag}.on must be one of: ${[...VALID_TRIGGERS].join(", ")}`);
      continue;
    }
    const run = errs.reqStr(entry, tag, "run");
    if (!run) continue;
    if (!actionIds.has(run) && !builtins.has(run)) {
      errs.add(
        `${tag}.run "${run}" is neither an [[actions]] id nor one of: ${AUTOMATION_BUILTINS.join(", ")}`,
      );
      continue;
    }
    // builtin:restack is the only run that targets a STACK rather than a
    // single worktree, and stack.parent_merged is the only trigger that
    // produces one — reject the mismatched pairings loudly.
    if (run === "builtin:restack" && on !== "stack.parent_merged") {
      errs.add(`${tag}: run "builtin:restack" requires on = "stack.parent_merged"`);
      continue;
    }
    if (run === "builtin:clean" && on === "stack.parent_merged") {
      errs.add(
        `${tag}: run "builtin:clean" targets one worktree; use "builtin:restack" for stack.parent_merged (it cleans merged members first)`,
      );
      continue;
    }
    const busy = errs.optEnum(
      entry,
      tag,
      "busy",
      ["queue", "skip"] as const satisfies readonly AutomationBusyPolicy[],
      "queue",
    );
    const cooldownRaw = entry.cooldown_minutes;
    if (
      cooldownRaw !== undefined &&
      !(typeof cooldownRaw === "number" && Number.isFinite(cooldownRaw) && cooldownRaw > 0)
    ) {
      errs.add(`${tag}.cooldown_minutes must be a positive number`);
      continue;
    }
    const settleRaw = entry.settle_seconds;
    if (
      settleRaw !== undefined &&
      !(typeof settleRaw === "number" && Number.isFinite(settleRaw) && settleRaw >= 0)
    ) {
      errs.add(`${tag}.settle_seconds must be a non-negative number`);
      continue;
    }
    seenIds.add(id);
    out.push({
      id,
      on: on as AutomationTrigger,
      run,
      busy,
      cooldownMinutes: typeof cooldownRaw === "number" ? cooldownRaw : null,
      settleSeconds:
        typeof settleRaw === "number" ? settleRaw : defaultSettleSeconds(on as AutomationTrigger),
    });
  }
  return out;
}

function parseActions(raw: unknown, errs: Errors): readonly ActionDef[] {
  if (raw === undefined) return GENERIC_DEFAULTS.actions;
  if (!Array.isArray(raw)) {
    errs.add("actions must be a TOML array of [[actions]] tables");
    return GENERIC_DEFAULTS.actions;
  }
  const out: ActionDef[] = [];
  const seenIds = new Set<string>();
  for (let i = 0; i < raw.length; i++) {
    const entry = obj(raw[i]);
    const tag = `actions[${i}]`;
    if (!entry) {
      errs.add(`${tag} must be a table`);
      continue;
    }
    const id = errs.reqStr(entry, tag, "id");
    const name = errs.reqStr(entry, tag, "name");
    if (!id || !name) continue;
    if (seenIds.has(id)) {
      errs.add(`${tag}.id "${id}" is duplicated`);
      continue;
    }
    const promptVal = entry.prompt;
    const shellVal = entry.shell;
    const hasPrompt = typeof promptVal === "string" && promptVal.length > 0;
    const hasShell = typeof shellVal === "string" && shellVal.length > 0;
    // Distinguish "wrong type / empty" from "missing entirely" so the
    // error names the real problem instead of demanding a field the
    // user already tried to set.
    if (promptVal !== undefined && !hasPrompt) {
      errs.add(`${tag}.prompt must be a non-empty string`);
      continue;
    }
    if (shellVal !== undefined && !hasShell) {
      errs.add(`${tag}.shell must be a non-empty string`);
      continue;
    }
    if (hasPrompt && hasShell) {
      errs.add(`${tag} must set exactly one of "prompt" or "shell", not both`);
      continue;
    }
    if (!hasPrompt && !hasShell) {
      errs.add(`${tag} must set one of "prompt" or "shell"`);
      continue;
    }
    const affects = parseAffects(entry.affects, tag, errs);
    if (affects === "invalid") continue;
    const requires = parseRequires(entry.requires, tag, errs);
    if (requires === "invalid") continue;
    // Optional picker affordances. `key` must be a single character;
    // `group` a non-empty label. Both default to undefined (auto-derived
    // key / ungrouped) when absent.
    const keyRaw = entry.key;
    if (keyRaw !== undefined && !(typeof keyRaw === "string" && keyRaw.length === 1)) {
      errs.add(`${tag}.key must be a single character`);
      continue;
    }
    const groupRaw = entry.group;
    if (groupRaw !== undefined && !(typeof groupRaw === "string" && groupRaw.length > 0)) {
      errs.add(`${tag}.group must be a non-empty string`);
      continue;
    }
    const ui: { key?: string; group?: string } = {};
    if (typeof keyRaw === "string") ui.key = keyRaw;
    if (typeof groupRaw === "string") ui.group = groupRaw;
    // `target` is prompt-action-only: it selects a supervised headless
    // primary-harness run (default) vs injecting into the live F12 session.
    // Reject it on shell actions so a misplaced field fails loud instead
    // of silently doing nothing.
    const targetRaw = entry.target;
    if (targetRaw !== undefined) {
      if (!hasPrompt) {
        errs.add(`${tag}.target is only valid on a claude (prompt) action`);
        continue;
      }
      if (targetRaw !== "session" && targetRaw !== "headless") {
        errs.add(`${tag}.target must be "session" or "headless"`);
        continue;
      }
    }
    const target: ClaudeActionTarget =
      targetRaw === "session" ? "session" : "headless";
    // `arg_prompt` is a single-line string ("company id") or omitted.
    // Stored as a small object so future extensions (placeholder text,
    // validation, multi-arg) don't churn the schema.
    const argPromptRaw = errs.optStrOrNull(entry, "arg_prompt");
    const argPrompt: ActionArgPrompt | null = argPromptRaw
      ? { label: argPromptRaw }
      : null;
    // `label_extract` is a regex source string. Compile-validate at
    // load time so a bad pattern fails the loader rather than silently
    // suppressing label extraction at run-time. Per-launch we recompile
    // (cheap; runs once per terminal action).
    const labelExtractRaw = errs.optStrOrNull(entry, "label_extract");
    if (labelExtractRaw !== null) {
      try {
        new RegExp(labelExtractRaw);
      } catch (err) {
        errs.add(`${tag}.label_extract: invalid regex (${(err as Error).message})`);
        continue;
      }
    }
    seenIds.add(id);
    if (hasPrompt) {
      out.push({
        kind: "claude",
        id,
        name,
        prompt: promptVal as string,
        target,
        affects: affects ?? DEFAULT_CLAUDE_AFFECTS,
        requires: requires ?? DEFAULT_REQUIRES,
        argPrompt,
        labelExtract: labelExtractRaw,
        ...ui,
      });
    } else {
      out.push({
        kind: "shell",
        id,
        name,
        shell: shellVal as string,
        affects: affects ?? DEFAULT_SHELL_AFFECTS,
        requires: requires ?? DEFAULT_REQUIRES,
        argPrompt,
        labelExtract: labelExtractRaw,
        ...ui,
      });
    }
  }
  return out;
}

function parseTagArray<T extends string>(
  raw: unknown,
  tag: string,
  field: "affects" | "requires",
  valid: ReadonlySet<T>,
  errs: Errors,
): readonly T[] | undefined | "invalid" {
  if (raw === undefined) return undefined;
  if (!Array.isArray(raw)) {
    errs.add(`${tag}.${field} must be an array of strings`);
    return "invalid";
  }
  const out: T[] = [];
  let bad = false;
  for (const v of raw) {
    if (typeof v !== "string") {
      errs.add(`${tag}.${field} entries must be strings`);
      bad = true;
      continue;
    }
    if (!valid.has(v as T)) {
      const allowed = [...valid].join(", ");
      errs.add(`${tag}.${field}: unknown tag "${v}" (allowed: ${allowed})`);
      bad = true;
      continue;
    }
    out.push(v as T);
  }
  if (bad) return "invalid";
  // Dedupe so `["git", "git"]` doesn't fan out to two invalidations
  // (or two precondition checks). Order is preserved.
  return [...new Set(out)];
}

function parseAffects(
  raw: unknown,
  tag: string,
  errs: Errors,
): readonly EffectTag[] | undefined | "invalid" {
  return parseTagArray(raw, tag, "affects", VALID_EFFECT_TAGS, errs);
}

function parseRequires(
  raw: unknown,
  tag: string,
  errs: Errors,
): readonly RequireTag[] | undefined | "invalid" {
  return parseTagArray(raw, tag, "requires", VALID_REQUIRE_TAGS, errs);
}

function load(): { cfg: Config; path: string } {
  const { path, present } = configPath();
  const errs = new Errors();
  const raw = loadRaw(path, present, errs);
  const cfg = build(raw, errs);
  errs.flush(path);
  return { cfg, path };
}

const loaded = load();

export const config: Config = Object.freeze(loaded.cfg) as Config;

/**
 * Assert SST is configured and return its config. Call from code paths
 * that only make sense with `[deploy.sst]` set (the `wt stages` command,
 * `core/sst.ts` helpers). Throws a user-readable error if not.
 */
export function requireSst(): SstConfig {
  if (!config.sst) {
    throw new Error("[deploy.sst] is not configured in config.toml");
  }
  return config.sst;
}
